# Take two numbers from user and perform arithmetic operations on them.

num1 = int(input("Enter first number: "))  # Taking user input - first number
num2 = int(input("Enter second number: "))  # Taking user input - second number

# performing arithmetic operations on user entered numbers
print(" \n The output of arithmetic operations, \n")
print("Addition Operation:", num1 + num2)  # Addition Operation
print("Subtraction Operation:", num1 - num2)  # Subtraction Operation
print("Multiplication Operation:", num1 * num2)  # Multiplication Operation
print("Division Operation:", num1 / num2)  # Division Operation
print("Floor Division:", num1 // num2)  # Floor Division
print("Exponential calculation:", num1 ** num2)  # Exponential calculation
# Modulus Operator - returns the remainder after division of 2 numbers
print("Modulus Operation:", num1 % num2)

